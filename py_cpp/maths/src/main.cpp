/*!
 * @file
 * Math functions definitions
 */


#include "main.h"

/*!
 * @brief Add two integers
 * @param a An integer
 * @param b Another integer
 */
int add(int a, int b) {
    return a + b;
}

/*!
 * @brief Subtract one integer from another
 * @param a An integer
 * @param b An integer to subtract from \p a
 */
int sub(int a, int b) {
    return a - b;
}
